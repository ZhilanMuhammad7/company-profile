let currentLang = 'id';

function toggleLanguage() {
    currentLang = currentLang === 'id' ? 'en' : 'id';
    document.getElementById('lang-btn').innerText = currentLang.toUpperCase();

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (content[currentLang][key]) {
            el.innerHTML = content[currentLang][key];
        }
    });
}

function openModal(projectId) {
    const modal = document.getElementById('projectModal');
    const body = document.getElementById('modal-body');
    const data = projectsData[projectId];
    const desc = currentLang === 'id' ? data.desc_id : data.desc_en;

    let imagesHtml = '<div class="modal-gallery">';
    data.images.forEach(img => {
        const imgClass = img.type === 'landscape' ? 'modal-img-landscape' : 'modal-img-portrait';
        imagesHtml += `<img src="${img.src}" class="${imgClass}" alt="Project Screenshot">`;
    });
    imagesHtml += '</div>';

    body.innerHTML = `
        <div class="modal-header">
            <h2>${data.title}</h2>
        </div>
        <div class="modal-meta">
            <div class="meta-item">
                <h4>Technology</h4>
                <p>${data.tech}</p>
            </div>
            <div class="meta-item">
                <h4>Role</h4>
                <p>${data.role}</p>
            </div>
        </div>
        <div class="modal-desc">
            <p>${desc}</p>
        </div>
        ${imagesHtml}
    `;

    modal.style.display = "block";
    document.body.style.overflow = "hidden";
}

function closeModal() {
    document.getElementById('projectModal').style.display = "none";
    document.body.style.overflow = "auto";
}

window.onclick = function (event) {
    const modal = document.getElementById('projectModal');
    if (event.target == modal) {
        closeModal();
    }
}

